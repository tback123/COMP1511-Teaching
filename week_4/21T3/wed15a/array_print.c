#include <stdio.h>

#define ARR_SIZE 20

void array_print(int length, int array[]);

int main(void) {

    // Initialise array
    int my_array[ARR_SIZE] = {0};

    // Print array
    array_print(ARR_SIZE, my_array);

    // END
    return 0;
}

// A function that takes in the length of an
// array and the array and prints the contents to STDOUT
void array_print(int length, int array[]) {

    int index = 0;
    while (index < length) {
        printf("%d\n", array[index]);
        index++;
    }

    return;
}